import streamlit as st
import matplotlib.pyplot as plt
import matplotlib.patches as patches
import matplotlib.path as Path # Wymaga, cho Path nie jest u偶ywany w tej wersji

def narysuj_dziadka_mroza_symbolicznie():
    """Rysuje symboliczn reprezentacj Dziadka Mroza za pomoc Matplotlib."""

    # 1. Konfiguracja p贸tna (rysunku)
    # Tworzymy figur, kt贸r Streamlit p贸藕niej wywietli
    fig, ax = plt.subplots(figsize=(6, 8))

    # Ustawienie limit贸w osi dla atwiejszego pozycjonowania
    ax.set_xlim(0, 100)
    ax.set_ylim(0, 100)

    # Ukrycie osi i ramek, by wygldao jak obraz
    ax.axis('off')

    # --- Kolory ---
    KOLOR_SZATY = '#005f99'    # Ciemnoniebieski
    KOLOR_BRODY = 'white'      # Biay
    KOLOR_BERLA = '#aaaaaa'    # Srebrny/szary
    KOLOR_TWARZY = '#f0c8a8'   # Jasna cera
    KOLOR_PASA = '#d4af37'     # Zoty/偶贸ty

    # --- Rysowanie element贸w (od ta do przodu) ---

    # Ciao (prostokt w kolorze szaty)
    cialo = patches.Rectangle((35, 20), 30, 50, color=KOLOR_SZATY, zorder=1)
    ax.add_patch(cialo)

    # Pas (cienki prostokt)
    pas = patches.Rectangle((33, 40), 34, 5, color=KOLOR_PASA, zorder=2)
    ax.add_patch(pas)

    # Gowa (koo)
    glowa = patches.Circle((50, 75), 10, color=KOLOR_TWARZY, zorder=3)
    ax.add_patch(glowa)

    # Czapka (tr贸jkt i pompon)
    # Zauwa偶, 偶e czapka jest rysowana nisko, zgodnie z Twoim oryginalnym kodem
    czapka_trojkat = patches.Polygon([[40, 75], [60, 75], [55, 95]], color=KOLOR_SZATY, zorder=4)
    ax.add_patch(czapka_trojkat)
    pompon = patches.Circle((55, 95), 3, color=KOLOR_BRODY, zorder=5)
    ax.add_patch(pompon)

    # Brody (du偶e biae koo/elipsa maskujce doln cz twarzy)
    broda = patches.Ellipse((50, 68), 20, 15, color=KOLOR_BRODY, zorder=4)
    ax.add_patch(broda)

    # Bero (cienka linia i gwiazda na g贸rze)
    # Linia bera
    ax.plot([70, 70], [20, 65], color=KOLOR_BERLA, linewidth=4, solid_capstyle='round', zorder=1)
    # G贸rny element bera (gwiazda/kula)
    berlo_gora = patches.Circle((70, 67), 3, color='silver', zorder=2)
    ax.add_patch(berlo_gora)

    # Oczy (mae czarne kropki)
    ax.plot(45, 78, 'o', color='black', markersize=3, zorder=6)
    ax.plot(55, 78, 'o', color='black', markersize=3, zorder=6)

    # Tytu
    ax.text(50, 9, 'SYMBOLICZNY DZIADEK MRZ', ha='center', fontsize=12, color='darkred')

    ax.set_title("Rysunek geometryczny Dziadka Mroza")

    # Zwracamy figur zamiast j wywietla
    return fig


