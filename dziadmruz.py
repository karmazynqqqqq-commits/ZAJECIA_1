def main():
    st.set_page_config(page_title="Dziadek Mr贸z w Streamlit", layout="centered")

    st.title(" Symboliczny Dziadek Mr贸z w Matplotlib i Streamlit")
    st.markdown("Poni偶ej znajduje si rysunek wygenerowany za pomoc Matplotlib i wywietlony przez Streamlit.")

    # 2. Wywietlenie obrazu w Streamlit
    figura_dziadka_mroza = narysuj_dziadka_mroza_symbolicznie()

